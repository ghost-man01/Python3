string = "SiddhantShukla"
print(string[:]) # From index 0 to string length

print(string[1:]) # From index 1 to string lenght

print(string[:8]) # From index 0 to 7

print(string[0:9]) # From index 0 to 8

print(string[9:14]) # From index 10 to 15

print(string[:-5]) # From 0 index to string_length-5

print(string[::-1]) # last character to first character

print(string[5:-6]) # from index 5 to -7

